package com.example.inheritanceExercise;

public class Member {
    String name;
    int age;
    long phone_number;
    String address;
    int salary;

    Member(String name, int age, long phone_number, String address, int salary) {
        this.name = name;
        this.age = age;
        this.phone_number = phone_number;
        this.address = address;
        this.salary = salary;
    }
}
